export function splitBeforeFirstFrame(lines: string[]): [string[], string[]] {
	const beforeFirstFrame: string[] = [];
	const remaining: string[] = [];
	let anyFrameSeen = false;
	for (const line of lines) {
		if (line.startsWith('frame')) {
			anyFrameSeen = true;
		}

		if (anyFrameSeen) {
			remaining.push(line);
		}
		else {
			beforeFirstFrame.push(line);
		}
	}

	return [beforeFirstFrame, remaining];
}

